package baekjoon;

import java.util.Scanner;

public class Problem1000 {
    public static void run() {
        // 스캐너 선언 후 초기화
        Scanner sc = new Scanner(System.in);
        // 정수 a, b 선언 후 nextInt()로 입력 받음
        int a = sc.nextInt();
        int b = sc.nextInt();
        // 두 정수 합 계산 후 출력
        System.out.println(a + b);
    }
}
